package lv.acodemy.classroom;

public class PlayWithWhileLoops {
    public static void main(String[] args) {

        // Print numbers from 1 to 10;

        int i = 1;
        while(i <= 10) {
            System.out.println(i);
            i++;
        }

        // Print even numbers from 2 to 20;

        int j = 2;
        while(j <= 20) {
            System.out.println(j);
            j += 2; // j = j + 2
        }

    }
}
